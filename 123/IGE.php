            <h2>Objetivo General:</h2>
            <p class="lead">Formar profesionales competentes en el diseño, desarrollo, implementación de negocios y gestión de proyectos con una visión sistémica, tecnológica y estratégica, ofreciendo soluciones innovadoras e integrales a las organizaciones, de acuerdo con las necesidades globales, actuales y emergentes, comprometidas con su entorno, desempeñándose con actitud ética, sustentable, emprendedora y de liderazgo.</p>

            <h2>Perfil de Ingreso</h2>
            <p class="lead">Los aspirantes a cursar la carrera de Ingeniería en Gestión Empresarial deben tener bachillerato terminado en las áreas económico-administrativas o en humanidades, ciencias sociales y políticas, preferentemente. Además, deberán contar con conocimientos básicos de investigación, física, química, matemáticas, y uso de las tecnologías de la información, de igual manera tener capacidad de razonamiento y abstracción, así como habilidad para trabajar en equipo, buena expresión oral y escrita y tener interés por el emprendedurismo.</p>

            <h2>Perfil de Egreso</h2>
            <p class="lead">• Desarrollar y aplicar habilidades directivas y la ingeniería en el diseño, creación, gestión, desarrollo, fortalecimiento e innovación de las organizaciones, con una orientación sistémica y sustentable para la toma de decisiones en forma efectiva.</p>
            <p class="lead">• Diseñar e innovar estructuras administrativas y procesos, con base en las necesidades de las organizaciones para competir eficientemente en mercados globales.</p>
            <p class="lead">• Gestionar eficientemente los recursos de la organización con visión compartida, con el fin de suministrar bienes y servicios de calidad.</p>
            <p class="lead">• Aplicar métodos cuantitativos y cualitativos para el análisis e interpretación de datos y modelado de sistemas, en los procesos organizacionales para la mejora continua, atendiendo estándares de calidad mundial.</p>
            <p class="lead">• Establecer programas para el fortalecimiento de la seguridad e higiene en las organizaciones.</p>
            <p class="lead">• Aplicar métodos de investigación para desarrollar e innovar sistemas, procesos y productos en las diferentes dimensiones de la organización.</p>

            <h2>Campo Laboral</h2>
            <p class="lead">El Ingeniero en Gestión Empresarial podrá desempeñar eficientemente funciones derivadas del área de producción, finanzas, recursos humanos, mercadotecnia y auditoría, asimismo podrá fungir como asesor o consultor de empresas, encargarse de la administración y gestión de empresas, públicas y privadas, diseñar productos de inversión, investigación de mercados, participar en la organización, planeación e integración organizacional de pequeñas y medianas empresas, además de desempeñarse como investigador y docente en instituciones educativas de nivel superior.</p>
