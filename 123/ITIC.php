        <ul>
            <li class="list lead">El campo laboral del Ingeniero en TIC's es muy amplio y puede prestar sus servicios profesionales en:</li>
            <li class="list lead"> Empresas fabricantes, de mantenimiento y servicios de equipo de cómputo.</li>
            <li class="list lead">Centros de cómputo, teleproceso y de telecomunicaciones privados y públicos.</li>
            <li class="list lead"> Empresas especializadas en el desarrollo de soluciones en hardware y software.</li>
            <li class="list lead"> Empresas especializadas en instalaciones de redes de corto, mediano y largo alcance.</li>
            <li class="list lead"> Empresas privadas y públicas dedicadas a capacitación en software de propósito general y especializado.</li>
