        <h2>Objetivo General:</h2>
        <p class="lead">Formar profesionistas capaces de integrar y administrar tecnologías de la información y comunicaciones, que contribuyan a la productividad y el logro de los objetivos estratégicos de las organizaciones; caracterizándose por ser líderes, críticos, competentes, éticos y con visión empresarial, comprometidos con el desarrollo sustentable.</p>
        <h2>Perfil Profesional:</h2>
        <ul>
            <li class="list lead"> Diseñar, implementar y administrar redes de cómputo y comunicaciones, bajo modelos y estándares internacionales, para satisfacer las necesidades de información de los sistemas sociales, garantizando aspectos de seguridad y calidad.</li>
            <li class="list lead"> Diseñar, desarrollar y mantener sistemas de bases de datos asegurando la integridad, disponibilidad y confidencialidad de la información.</li>
            <li class="list lead"> Integrar las diferentes arquitecturas de hardware y administrar software para incrementar la productividad en organizaciones.</li>
            <li class="list lead"> Implementar sistemas de seguridad bajo políticas internas de las organizaciones y estándares aceptados.</li>
            <li class="list lead"> Administrar proyectos que involucren Tecnologías de la Información y Comunicaciones en las organizaciones conforme a requerimientos.</li>
            <li class="list lead"> Observar los aspectos legales del uso y explotación de las Tecnologías de la Información y Comunicaciones (TIC).</li>
            <li class="list lead"> Desarrollar e implementar sistemas de información para el control y la toma de decisiones utilizando metodologías basadas en estándares internacionales.</li>
            <li class="list lead"> Analizar, diseñar y programar dispositivos con software empotrado.</li>
            <li class="list lead"> Utilizar tecnologías y herramientas actuales y emergentes acordes a las necesidades del entorno.</li>
            <li class="list lead"> Integrar soluciones basadas en sistemas de comunicaciones que involucren tecnologías actuales y emergentes. Crear empresas en el ámbito de las TIC.</li>
            <li class="list lead"> Desempeñar funciones de consultoría y auditoría en el campo de las TIC.</li>
        </ul>
